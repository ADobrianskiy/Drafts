public class Pair {
    public int firstX;
    public int firstY;
    public int secondX;
    public int secondY;

    public Pair (int firstX, int firstY, int secondX, int secondY){
        this.firstX = firstX; this.firstY = firstY;
        this.secondX = secondX; this.secondY = secondY;
    }
}
